package model;

/**
 *
 * @author ITU
 */
public class Revient {
    String Id;
    float Min;
    float Max;
    float Pourcentage;

    public String getId() {
        return Id;
    }

    public void setId(String Id) {
        this.Id = Id;
    }

    public float getMin() {
        return Min;
    }

    public void setMin(float Min) {
        this.Min = Min;
    }

    public float getMax() {
        return Max;
    }

    public void setMax(float Max) {
        this.Max = Max;
    }

    public float getPourcentage() {
        return Pourcentage;
    }

    public void setPourcentage(float Pourcentage) {
        this.Pourcentage = Pourcentage;
    }
    
    
}
